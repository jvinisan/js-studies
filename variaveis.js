console.log('Entendendo variáveis em JS: ')

// Salvando dado na variável
const idade = 24

// Imprimindo na tela
console.log(idade)

// Imprimindo a variável com operações
console.log(idade + 2)
console.log(idade - 2)
console.log(idade * 2)
console.log(idade / 2)

// Guardando uma operação em uma variável
const idadeSomada = idade + 2
console.log(idadeSomada)

// Imprimindo uma string
console.log('idade')

// Imprimindo com interpolação
let nome = 'João'
const sobrenome = 'Vinícius'

nome = nome + sobrenome
console.log(`Meu nome é ${nome}`)
